package com.nordson.MDS;

public class MDSGetterandSetters_Flow {
	// Declaration of Temperature MDS variables to set and get the Values
			private static String units1 = "";
			private static String default1="";
			private static String min1 = "";
			private static String max1 = "";
			private static String targetAddon="";
		
		public String getUnits1() {
			return units1;
		}

		public void setUnits1(String units1) {
			this.units1 = units1;
		}

		public String getDefault1() {
			return default1;
		}

		public void setDefault1(String default1) {
			this.default1 = default1;
		}

		public String getMin1() {
			return min1;
		}

		public void setMin1(String min1) {
			this.min1 = min1;
		}

		public String getMax1() {
			return max1;
		}

		public void setMax1(String max1) {
			this.max1 = max1;
		}
		
		public String getTargetAddon() {
			return targetAddon;
		}

		public void setTargetAddon(String targetAddon) {
			this.targetAddon = targetAddon;
		}


	}
